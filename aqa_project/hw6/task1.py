import re
# Напишіть програму яка перевіряє чи стрічка містить лише великі і малі літери, числа та нижнє підкреслення

demo_string1 = 'Test_string8'
pattern = '^[a-zA-Z0-9_]+$'
if re.match(pattern, demo_string1):
    print('Стрічка містить лише великі, малі літери, числа та нижнє підкреслення')
else:
    print('Стрічка не відповідає умові')
